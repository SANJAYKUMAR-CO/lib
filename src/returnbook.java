    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
 String url = "jdbc:mysql://localhost:3306/library"; // Your database URL
    String mysqlUser = "sanjay"; // Your MySQL username
    String mysqlPwd = "sanjay"; // Your MySQL password

    String studentId = studentField.getText();

    if (studentId.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a Student ID.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    String searchQuery = "SELECT bookid, issuedate, duedate FROM issue WHERE studentid = ? AND returnbook = 'no'";

    try (Connection conn = DriverManager.getConnection(url, mysqlUser, mysqlPwd);
         PreparedStatement searchStmt = conn.prepareStatement(searchQuery)) {

        searchStmt.setString(1, studentId);

        try (ResultSet rs = searchStmt.executeQuery()) {
            if (rs.next()) {
                String bookId = rs.getString("bookid");
                 String issueDate = rs.getString("issuedate");
                String dueDate = rs.getString("duedate");

                bookIdField.setText(bookId);
                date.setText(issueDate);
                date1.setText(dueDate);
            } else {
                JOptionPane.showMessageDialog(this, "No active issue found for this Student ID.", "Not Found", JOptionPane.INFORMATION_MESSAGE);
            }
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

