    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String url = "jdbc:mysql://localhost:3306/library"; // Your database URL
    String mysqlUser = "sanjay"; // Your MySQL username
    String mysqlPwd = "sanjay"; // Your MySQL password

    String bookId = bookIdField.getText();
    String studentId = studentField.getText();

    String updateQuery = "UPDATE issue SET returnbook = 'yes' WHERE bookid = ? AND studentid = ?";

    try (Connection conn = DriverManager.getConnection(url, mysqlUser, mysqlPwd);
         PreparedStatement updateStmt = conn.prepareStatement(updateQuery)) {

        updateStmt.setString(1, bookId);
        updateStmt.setString(2, studentId);

        int rowsUpdated = updateStmt.executeUpdate();
        if (rowsUpdated > 0) {
            JOptionPane.showMessageDialog(this, "Book returned successfully!");
        } else {
            JOptionPane.showMessageDialog(this, "No matching record found. Please check the Book ID and Student ID.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

